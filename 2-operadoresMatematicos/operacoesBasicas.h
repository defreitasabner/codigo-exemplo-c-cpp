// Exemplo de função que realiza a adição
int adicao(int primeiroNumero, int segundoNumero) {
    return primeiroNumero + segundoNumero;
}

// Exemplo de função que realiza a subtração
int subtracao(int primeiroNumero, int segundoNumero) {
    return primeiroNumero - segundoNumero;
}

// Exemplo de função que realiza a multiplicação
int multiplicao(int primeiroNumero, int segundoNumero) {
    return primeiroNumero * segundoNumero;
}

// Exemplo de função que realiza a divisão
// Divisão padrão sempre retorna um float
float divisao(int dividendo, int divisor) {
    return dividendo / divisor;
}

// Exemplo de função que retorna o resto da divisão
int restoDaDivisao(int dividendo, int divisor) {
    return dividendo % divisor;
}